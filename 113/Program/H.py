Dict = {
    "A":"10", "J":"18", "S":"26",
    "B":"11", "K":"19", "T":"27",
    "C":"12", "L":"20", "U":"28",
    "D":"13", "M":"21", "V":"29",
    "E":"14", "N":"22", "W":"32",
    "F":"15", "O":"35", "X":"30",
    "G":"16", "P":"23", "Y":"31",
    "H":"17", "Q":"24", "Z":"33",
    "I":"34", "R":"25",
}

a = input()
Eng = int(Dict[a[0]][0]) + int(Dict[a[0]][1])*9

a = a[1::]
kk = 8
for i in a:
    if kk==0:
        Eng += int(i)
        break
    Eng += int(i)*kk
    kk -= 1

if Eng % 10 == 0:
    print("Y")
else:
    print("N")
